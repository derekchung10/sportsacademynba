export function ChannelIcon({ channel, className = 'w-3 h-3' }) {
  if (channel === 'voice') return <PhoneIcon className={className} />;
  if (channel === 'sms') return <SmsIcon className={className} />;
  return <EmailIcon className={className} />;
}

export function EventIcon({ eventType, className = 'w-4 h-4' }) {
  if (eventType === 'interaction_completed') return <PhoneIcon className={className} />;
  if (eventType === 'status_changed') return <ArrowUpDownIcon className={className} />;
  if (eventType === 'nba_produced') return <ChevronRightIcon className={className} />;
  if (eventType === 'context_enriched') return <LightBulbIcon className={className} />;
  return <PlusIcon className={className} />;
}
